package loopingstatments;

public class ExampleForLoop {

	public static void main(String[] args) {
				//Hello 5 times
				for(int i=1;i<=5;i++)
				{
					System.out.println("Hello");
				}
				//Print 5,4,3,2,1
				for(int i=5;i>=1;i--)
				{
					System.out.println(i);
				}
				//Print 2,4,6,8,10
				for(int i=2;i<=10;i=i+2)
				{
					System.out.println(i);
				}

	}

}
